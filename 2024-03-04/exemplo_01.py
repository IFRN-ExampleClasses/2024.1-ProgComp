'''
Fazer um programa que calcule o perímetro e a 
área de um círculo
'''

raio = 5.75

area = 3.14 * raio ** 2
perimetro = 2 * 3.14 * raio

print(area)
print(perimetro)
print('')

print('Área = ', area)
print('Perímetro = ', perimetro)
print('')

# O círculo de raio XYZ tem área = xyzi
print('O círculo de raio', raio, 'tem área = ',area)
print('O círculo de raio', raio, 'tem perímetro = ',perimetro)
print('')

print(f'O círculo de raio {raio:.3f} tem área = {area:.3f}')
print(f'O círculo de raio {raio:.3f} tem perímetro = {perimetro:.3f}')